            if (bulan == 0) {

                JOptionPane.showMessageDialog(rootPane, "Please select month", "WARNING", 0);
            }
            if (bulan == 1) {
                month = "01";
            }
            if (bulan == 2) {
                month = "02";
            }
            if (bulan == 3) {
                month = "03";
            }
            if (bulan == 4) {
                month = "04";
            }
            if (bulan == 5) {
                month = "05";
            }
            if (bulan == 6) {
                month = "06";
            }
            if (bulan == 7) {
                month = "07";
            }
            if (bulan == 8) {
                month = "08";
            }
            if (bulan == 9) {
                month = "09";
            }
            if (bulan == 10) {
                month = "10";
            }
            if (bulan == 11) {
                month = "11";
            }
            if (bulan == 12) {
                month = "12";
            }
            if (m.getSelectedIndex() == 0) {
                psmt.setDate(5, null);
            } else {
                String petsa = tuig + "-" + month + "-" + adlaw;
                psmt.setString(5, petsa);
            }

            psmt.setString(6, to.getText());

            psmt.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "SUCCEESSFULLY ADDED");

            from.setText("");
            type.setSelectedIndex(0);
            statusgroup.clearSelection();
            to.setText("");
